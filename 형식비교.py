# List (리스트)
my_list = [1, 2, 3, 4, 5]

# Tuple (튜플)
my_tuple = (1, 2, 3, 4, 5)

# Dictionary (딕셔너리)
my_dict = {'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5}

# 출력
print("List:")
for item in my_list:
    print(item)

print("\nTuple:")
for item in my_tuple:
    print(item)

print("\nDictionary:")
for key, value in my_dict.items():
    print(f"{key}: {value}")
